"""
Пример 1: Работа с данными о человеке
"В этом примере мы рассмотрим, как использовать словари для хранения информации о человеке. Вы научитесь создавать словарь, получать данные, добавлять новые значения и изменять существующие."
"""
# Создаём словарь
person = {
    "name": "Alice",
    "age": 30,
    "city": "New York"
}

# Доступ к данным
print("Имя:", person["name"])
print("Возраст:", person["age"])
print("Город:", person["city"])

# Добавление новой информации
person["job"] = "Engineer"

# Обновление данных
person["age"] = 31

# Перебор всех данных
print("\nИнформация о человеке:")
for key, value in person.items():
    print(f"{key}: {value}")

# Удаление данных
person.pop("city")  # Удаляем информацию о городе
print("\nОбновлённый словарь:", person)



"""
Пример 2: Управление инвентарём в игре
"Теперь давайте посмотрим, как использовать словари для управления игровым инвентарём. Мы научимся добавлять новые предметы, изменять их количество и удалять ненужные."

python
Копировать
Редактировать

"""
# Инвентарь игрока
inventory = {
    "sword": 1,
    "potion": 5,
    "shield": 1
}

# Вывод текущего инвентаря
print("Инвентарь:")
for item, count in inventory.items():
    print(f"{item}: {count}")

# Добавляем новый предмет
inventory["arrows"] = 10

# Используем зелье
inventory["potion"] -= 1

# Удаляем сломанный меч
inventory.pop("sword")

# Итоговый инвентарь
print("\nОбновлённый инвентарь:")
for item, count in inventory.items():
    print(f"{item}: {count}")



"""
Пример 3: Работа со студентами и их оценками
"Последний пример покажет, как можно использовать словари для работы с оценками студентов. Мы добавим нового студента, изменим оценки и выведем итоговый список."
"""
# Список студентов
students = {
    "Alice": 85,
    "Bob": 78,
    "Charlie": 90
}

# Добавляем нового студента
students["Diana"] = 50

# Обнавление оценок
students["Alice"] = 95

# Вывод всех студентов
print("Оценки студентов")
for name, grade in students.items():
    print(f"{name}: {grade}")


# Проверка наличие студентов
if "Eve" not in students:
    print("\nEve ещё нет в списке")

# Удаляем студента
students.pop("Bob")

# Новый словарь
print("\nИтоговый список студентов")
for name, grade in students.items():
    print(f"{name}: {grade}")
